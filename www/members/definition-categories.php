          <p class=smalltext>Products that promote methods of promotion using search engines through optimization, page ranking, keywords,
          placement, or inclusion belong in this Product Category. Although products associated with Blogging, Social Media, and Web 2.0
          belong in the next category, Web Marketing, the product or service may be better suited in Search Marketing category if the goal
          or subject matter is primarily focussed on producing higher rankings in Search engines.</p>

              <span class=red><b>Web Marketing:</b></span>
          <p class=smalltext>This category covers a broad range of products associated with online marketing. Products or services which
          provide instruction or methods on how to market or advertise something on the Internet, belong in this category. Examples of
          topics dealing with this category are blogging, video marketing, social networking, affiliate marketing, display or banner
          advertising, interactive or contextual marketing, viral marketing, email marketing, lead generation, market research &
          development,  etc. </p>

              <span class=red><b>Website Tools:</b></span>
          <p class=smalltext>Products or services that provide instruction or software tools for enhancing a website's look & feel,
          it's function, or it's marketability should be included in this category. Examples of products and services that belong in
          this category are those providing instruction or tools for audeo/video, html creation or editing, popup creators or blockers,
          software languages such as javascript, PHP, mySQL, CSS, XML, DHTML, etc.</p>

              <span class=red><b>List Building Tools:</b></span>
          <p class=smalltext>Any product or service that promotes or instructs on methods of building a subscriber list should be
                  found in this category. Examples include tools or instruction in database/list management, marketing techniques to entice
          visitors to join or subscribe to your list (including various popup techniques), the use of email, autoresponders,
          newsletters, ezines, followup messages, etc. </p>

              <span class=red><b>MLM Opportunities:</b></span>
          <p class=smalltext>Products and services that either give instruction on, or are promoted through multi-level marketing
          (MLM) belong in this category. Any business with an affiliate marketing structure of 2 or more levels is technically
          considered multi-level marketing ("multi" means more than one level). These payment or marketing structures include (but
          not limited to) unilevel, stairstep breakaway, binary, matrix, australian 1up/2up, etc.</p>

              <span class=red><b>Business Instruction:</b></span>
          <p class=smalltext>This category includes any product or service that gives instruction on creating, maintaining, or
          operating a business, whether online or offline. Topics could include taxes, business plans, business structure,
          costs, implementation, pros & cons, resources for funding, assistance, employees vs outsourcing, lists of necessary
          components of different types of businesses, planing, projections, government and private sources, etc.</p>

              <span class=red><b>Niches:</b></span>
          <p class=smalltext>Any niche product, service, or business that addresses the needs of a market segment not met by any of
          the previous 6 categories (primarily anything NOT having to do with Internet Marketing), belongs in this category. Examples
          include products that promote methods of niche marketing, as well as types of niches (offline & online), such as mortgage
          refinancing, debt consolidation, windsheild repair, dog grooming, etc. A niche product or service may also include
          instruction, training, guides, DVDs, audio/video, books or any other medium on self help (such as motivation, relationships,
          attitude, etc.), travel, arts & crafts, or any other topic, whether for profit, or not for profit.</p>

